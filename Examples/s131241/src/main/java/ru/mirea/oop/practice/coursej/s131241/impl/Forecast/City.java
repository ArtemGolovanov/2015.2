package ru.mirea.oop.practice.coursej.s131241.impl.Forecast;

public class City {
    private long id;
    private String name;
    private Coord coord;
    private String country;
    private long population;

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public Coord getCoord() {
        return coord;
    }

    public String getCountry() {
        return country;
    }

    public long getPopulation() {
        return population;
    }

}
